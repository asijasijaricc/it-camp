//  Number Properties (Constants) Konstante //

// Number konstante ne mogu biti koriscene u varijablama
// Sledecim konstantama (Number svojstvima) mozemo pristupiti iskljucivo preko:
// NUmber.naziv_konstante

// MAX_VALUE - je najveci moguci broj u JavaScriptu.

console.log(Number.MAX_VALUE);

// MIN_VALUE - je najmanji moguci broj u JavaScriptu

console.log(Number.MIN_VALUE);

// POSITIVE.INFINITY - Infinitiy (pozitivna beskonacnost)

console.log(Number.POSITIVE_INFINITY);

// NEGATIVE.INFINITY - Infinitiy (negativna beskonacnost)

console.log(Number.NEGATIVE_INFINITY);

// NaN - Not a Number vrednost

console.log(Number.NaN);
